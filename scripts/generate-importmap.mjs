// Custom admin components referenced via path strings in payload.config.ts.
// key   = the value in payload.config.ts (used as the importMap lookup key)
// rel   = relative import path from app/(payload)/admin/importMap.js
//         Must NOT use server-relative '/' imports — Turbopack rejects them.
const customComponents = [
  { key: '/components/admin/Logo',             rel: '../../../components/admin/Logo',             alias: 'Logo_custom' },
  { key: '/components/admin/Icon',             rel: '../../../components/admin/Icon',             alias: 'Icon_custom' },
  { key: '/components/admin/DashboardStats',   rel: '../../../components/admin/DashboardStats',   alias: 'DashboardStats_custom' },
  { key: '/components/admin/AnalyticsDashboard', rel: '../../../components/admin/AnalyticsDashboard', alias: 'AnalyticsDashboard_custom' },
];

const imports = [
  ...components.map(([name, pkg], i) => `import { ${name} as ${name}_${i} } from '${pkg}';`),
  ...customComponents.map(({ rel, alias }) => `import ${alias} from '${rel}';`),
].join('\n');

const entries = [
  ...components.map(([name, pkg], i) => `  '${pkg}#${name}': ${name}_${i}`),
  ...customComponents.map(({ key, alias }) => `  '${key}#default': ${alias}`),
].join(',\n');

const content = `/* THIS FILE WAS GENERATED BY scripts/generate-importmap.mjs */
${imports}

export const importMap = {
${entries},
};
`;

writeFileSync(outputPath, content);
console.log('Generated importMap.js successfully');
