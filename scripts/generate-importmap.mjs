/**
 * Generates the Payload CMS importMap.js without using tsx.
 * This bypasses the Node 24 ESM/CJS incompatibility with tsx.
 *
 * Based on the plugins in payload.config.ts:
 * - @payloadcms/richtext-lexical (editor)
 * - @payloadcms/storage-s3 (media storage)
 */
import { writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outputPath = resolve(__dirname, '../app/(payload)/admin/importMap.js');

// These are the client components required by our Payload config.
// S3 storage plugin components
const s3Components = [
  ['S3ClientUploadHandler', '@payloadcms/storage-s3/client'],
];

// Lexical rich text editor components
const lexicalComponents = [
  ['RichTextCell', '@payloadcms/richtext-lexical/client'],
  ['RichTextField', '@payloadcms/richtext-lexical/client'],
  ['InlineToolbarFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['FixedToolbarFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['BoldFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['ItalicFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['UnderlineFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['StrikethroughFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['SubscriptFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['SuperscriptFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['InlineCodeFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['ParagraphFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['HeadingFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['AlignFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['IndentFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['UnorderedListFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['OrderedListFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['ChecklistFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['LinkFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['RelationshipFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['BlockquoteFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['UploadFeatureClient', '@payloadcms/richtext-lexical/client'],
  ['HorizontalRuleFeatureClient', '@payloadcms/richtext-lexical/client'],
];

const allComponents = [...s3Components, ...lexicalComponents];

// Generate import statements
const imports = allComponents
  .map(([name, pkg], i) => `import { ${name} as ${name}_${i} } from '${pkg}';`)
  .join('\n');

// Generate map entries
const entries = allComponents
  .map(([name, pkg], i) => `  '${pkg}#${name}': ${name}_${i}`)
  .join(',\n');

const content = `/* THIS FILE WAS GENERATED BY scripts/generate-importmap.mjs */
${imports}

export const importMap = {
${entries},
};
`;

writeFileSync(outputPath, content);
console.log('Generated importMap.js successfully');
